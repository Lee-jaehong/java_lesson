package koreait01;

public class C01_Print {
    
	//프로그램 실행 - main 메소드가 필요합니다.
	public static void main(String[] args) {
	
				
		System.out.println();  // 화면에 출력
		System.out.println("하이!");  // 문자열 데이터 (큰따옴표)
		System.out.println(3.14); // 실수 데이터
		System.out.println(100); // 정수 데이터
		System.out.println(34+567);  // 수식(덧셈)
		System.out.println(true); // 논리(boolean) 데이터
		System.out.println("하이!!" + "자바야~"); // 문자열의 + 기능 - 연결기능
		System.out.println("나는" + 23 + "세 입니다."); // 문자 + 정수형 - 연결기능
	}
}

// 단축키
//1) ctrl + 스페이스바 - sysout 하고 입력하면 system.out.println() 자동 호출
